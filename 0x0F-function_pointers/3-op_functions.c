#include "3-calc.h"

/**
 * op_add - suma dos numeros
 * @a: primer número
 * @b: segundo número
 * Return: la suma
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - resta dos números
 * @a: primer número
 * @b: segundo número
 * Return: la resta
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplica dos números
 * @a: primer número
 * @b: segundo número
 * Return: el producto
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide dos números
 * @a: primer número
 * @b: segundo número
 * Return: cociente
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - saca el módulo de dos números
 * @a: primer número
 * @b: segundo número
 * Return: el módulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
